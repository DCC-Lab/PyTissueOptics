# Parse MCX data
mcx_lines = mcx_data.strip().split('\n')[1:]  # Skip the header
mcx_g_values = []
mcx_absorbed_values = []

for line in mcx_lines:
    parts = line.split()
    g = float(parts[1])
    absorbed = float(parts[3].strip('%'))
    mcx_g_values.append(g)
    mcx_absorbed_values.append(absorbed)

# Parse pytissueoptics data
